public class Main {
    public static void main(String[] args) {
        System.out.printf("%S\n", "jim");
        System.out.printf("%c %C\n", 'c','c');
        System.out.printf("%x %X\n", 10, 10);
        System.out.printf("%o\n", 10);
        System.out.printf("%e %E\n", 10.0, 10.0);
        System.out.printf("%b %B\n", true, false);
    }
}

